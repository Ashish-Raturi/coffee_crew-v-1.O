import 'screens/authentication/authentication.dart';

class Wrapper extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    final user = Provider.of<User>(context);
    if (user == null) {
      return Authenticaion();
    } else {
      return Home();
    }
  }
}
